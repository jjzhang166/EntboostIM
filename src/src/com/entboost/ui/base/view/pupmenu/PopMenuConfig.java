package com.entboost.ui.base.view.pupmenu;

public class PopMenuConfig {
	private int width;//菜单宽度
	private int background_resId;//背景
	private int showAsDropDownYoff;
	private int textColor;//字体颜色
	private int textSize;//字体大小

	public int getShowAsDropDownYoff() {
		return showAsDropDownYoff;
	}

	public void setShowAsDropDownYoff(int showAsDropDownYoff) {
		this.showAsDropDownYoff = showAsDropDownYoff;
	}

	public int getWidth() {
		return width;
	}

	public void setWidth(int width) {
		this.width = width;
	}

	public int getBackground_resId() {
		return background_resId;
	}

	public void setBackground_resId(int background_resId) {
		this.background_resId = background_resId;
	}

	public int getTextColor() {
		return textColor;
	}

	public void setTextColor(int textColor) {
		this.textColor = textColor;
	}

	public int getTextSize() {
		return textSize;
	}

	public void setTextSize(int textSize) {
		this.textSize = textSize;
	}

	
}
